package main

import (
	"fmt"
)

func main() {

	var s []int
	printSlice(s)

	for i := int(0); i <= 30; i++ {
		s = append(s, i)
		printSlice(s)
	}

}

func printSlice(s []int) {
	fmt.Printf("%v => len: %d, cap: %d\n", s, len(s), cap(s))
}

